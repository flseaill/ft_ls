#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int nb;

	nb = (unsigned int)n;
	if (n < 0)
	{
		write(1, "-", 1);
		nb = n * -1;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		nb += '0';
		write(1, &nb, 1);
	}
}
